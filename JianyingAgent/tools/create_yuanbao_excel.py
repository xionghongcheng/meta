from pathlib import Path

from openpyxl import Workbook
from openpyxl.styles import Alignment, Border, Font, PatternFill, Side
from openpyxl.utils import get_column_letter


def find_target_folder() -> Path:
    base = Path("C:/Users/Administrator/Desktop")
    docs = [
        p
        for p in base.rglob("*.docx")
        if "plugins" in str(p)
        and "(2)" in str(p.parent)
        and not p.name.startswith("~$")
    ]
    if not docs:
        raise RuntimeError("未找到目标文件夹里的 docx 文件")
    return docs[0].parent


def style_sheet(ws, widths, row_height=30):
    thin = Side(style="thin", color="D9D9D9")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    header_fill = PatternFill("solid", fgColor="EAF2F8")

    for cell in ws[1]:
        cell.font = Font(bold=True)
        cell.fill = header_fill
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        cell.border = border

    for row in ws.iter_rows(min_row=2):
        for cell in row:
            cell.alignment = Alignment(vertical="top", wrap_text=True)
            cell.border = border

    for index, width in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(index)].width = width
    for index in range(1, ws.max_row + 1):
        ws.row_dimensions[index].height = row_height
    ws.freeze_panes = "A2"


